/* Include libraries ---------------------------------------------------------*/

#include "snake.h"

/* Main function -------------------------------------------------------------*/

int main(void)
{
    /* Game initialization ---------------------------------------------------*/

    snake_init();

    /* Main game loop --------------------------------------------------------*/

    while (!snake_exit())
    {
        /* Update ------------------------------------------------------------*/

        snake_update_matrix(&snake, &food);

        /* Begin drawing -----------------------------------------------------*/

        snake_draw();

        /* End drawing -------------------------------------------------------*/
    }

    snake_deinit();
    return 0;
}

